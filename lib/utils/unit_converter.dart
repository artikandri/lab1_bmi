double feetToCentimeter(double digitInFeet) {
  return digitInFeet * 30.48;
}

double centimeterToFeet(double digitInMeter) {
  return digitInMeter * 0.0328084;
}

double feetToMeter(double digitInFeet) {
  return digitInFeet * 0.3048;
}

double meterToFeet(double digitInMeter) {
  return digitInMeter * 3.28084;
}

double kilogramToPound(double digitInKilo) {
  return digitInKilo * 2.20462;
}

double poundToKilogram(double digitInPound) {
  return digitInPound * 0.453592;
}
